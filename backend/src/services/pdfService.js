const PDFDocument = require('pdfkit');
const fs = require('fs');
const path = require('path');

function ensurePdfDir() {
  const dir = path.join(__dirname, '../../public/pdfs');
  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
  return dir;
}

function formatDate(dateStr) {
  // Parse as UTC to avoid local-timezone off-by-one on day names/numbers
  const s = typeof dateStr === 'string' ? dateStr : dateStr.toISOString().split('T')[0];
  const [year, month, day] = s.split('-').map(Number);
  const d = new Date(Date.UTC(year, month - 1, day));
  const days = ['Sunday', 'Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday'];
  const months = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'];
  return `${days[d.getUTCDay()]}, ${d.getUTCDate()} ${months[d.getUTCMonth()]}`;
}

function drawHeader(doc, subtitle) {
  doc.rect(0, 0, doc.page.width, 75).fill('#16a34a');
  doc.fill('white').fontSize(22).font('Helvetica-Bold').text('CookConnect', 50, 18);
  doc.fontSize(11).font('Helvetica').text(subtitle, 50, 48);
  doc.y = 95;
}

function drawDivider(doc, color = '#e5e7eb') {
  doc.moveTo(50, doc.y).lineTo(doc.page.width - 50, doc.y).strokeColor(color).stroke();
  doc.moveDown(0.4);
}

async function generateWeeklyPDF(user, planDays, familyCount = 1) {
  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({ margin: 50, size: 'A4' });
    const dir = ensurePdfDir();
    const filename = `weekly-plan-${user.id}-${Date.now()}.pdf`;
    const filepath = path.join(dir, filename);
    const stream = fs.createWriteStream(filepath);
    doc.pipe(stream);

    const sorted = [...planDays].sort((a, b) => a.day_number - b.day_number).slice(0, 7);
    const startDate = sorted[0] ? formatDate(sorted[0].day_date) : '';
    const endDate = sorted[sorted.length - 1] ? formatDate(sorted[sorted.length - 1].day_date) : '';

    drawHeader(doc, 'Weekly Meal Plan');

    doc.fill('#1f2937').fontSize(13).font('Helvetica-Bold')
      .text(`Family: ${user.family_name || user.name}`);
    doc.fill('#6b7280').fontSize(10).font('Helvetica')
      .text(`Period: ${startDate} — ${endDate}  ·  ${familyCount} member${familyCount !== 1 ? 's' : ''}`);
    doc.moveDown(0.6);
    drawDivider(doc);

    function getWeeklyMealItems(day, mealType) {
      const fromMealItems = (day.mealItems || [])
        .filter(mi => mi.meal_type === mealType && mi.menuItem)
        .map(mi => mi.menuItem);
      if (fromMealItems.length > 0) return fromMealItems;
      if (mealType === 'breakfast' && day.breakfast) return [day.breakfast];
      if (mealType === 'lunch' && day.lunch) return [day.lunch];
      if (mealType === 'dinner' && day.dinner) return [day.dinner];
      return [];
    }

    sorted.forEach(day => {
      if (doc.y > 660) { doc.addPage(); doc.y = 50; }

      const bItems = getWeeklyMealItems(day, 'breakfast');
      const lItems = getWeeklyMealItems(day, 'lunch');
      const dItems = getWeeklyMealItems(day, 'dinner');

      const sumCal = items => items.reduce((s, i) => s + (i.calories_per_serving || 0), 0) * familyCount;
      const bCal = sumCal(bItems), lCal = sumCal(lItems), dCal = sumCal(dItems);
      const totalCal = bCal + lCal + dCal;

      const nameList = items => items.length ? items.map(i => i.name).join(', ') : '—';

      // Day heading
      doc.fill('#16a34a').fontSize(11).font('Helvetica-Bold').text(formatDate(day.day_date));
      doc.moveDown(0.2);

      const col1 = 50, col2 = 140, col3 = 390;
      const rowH = 16;
      const startY = doc.y;

      doc.fill('#6b7280').fontSize(9).font('Helvetica-Bold');
      doc.text('Breakfast', col1, startY);
      doc.text(nameList(bItems), col2, startY, { width: 240 });
      doc.text(`${bCal} cal`, col3, startY);

      doc.text('Lunch', col1, startY + rowH);
      doc.text(nameList(lItems), col2, startY + rowH, { width: 240 });
      doc.text(`${lCal} cal`, col3, startY + rowH);

      doc.text('Dinner', col1, startY + rowH * 2);
      doc.text(nameList(dItems), col2, startY + rowH * 2, { width: 240 });
      doc.text(`${dCal} cal`, col3, startY + rowH * 2);

      doc.fill('#059669').fontSize(9).font('Helvetica-Bold')
        .text(`Total: ${totalCal} kcal`, col3, startY + rowH * 3);

      doc.y = startY + rowH * 4 + 6;
      drawDivider(doc, '#f3f4f6');
    });

    doc.moveDown(0.5);
    doc.fill('#9ca3af').fontSize(8).font('Helvetica')
      .text(
        `Generated by CookConnect · ${new Date().toLocaleDateString('en-IN')} · BF 7:30AM | LU 12:30PM | DI 7:30PM`,
        { align: 'center' }
      );

    doc.end();
    stream.on('finish', () => resolve({ filename, filepath }));
    stream.on('error', reject);
  });
}

async function generateTodayPDF(user, planDay, familyCount = 1) {
  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({ margin: 50, size: 'A4' });
    const dir = ensurePdfDir();
    const filename = `today-menu-${user.id}-${Date.now()}.pdf`;
    const filepath = path.join(dir, filename);
    const stream = fs.createWriteStream(filepath);
    doc.pipe(stream);

    drawHeader(doc, "Today's Menu");

    doc.fill('#1f2937').fontSize(13).font('Helvetica-Bold')
      .text(`Family: ${user.family_name || user.name}`);
    if (planDay) {
      doc.fill('#6b7280').fontSize(10).font('Helvetica')
        .text(`Date: ${formatDate(planDay.day_date)}  ·  ${familyCount} member${familyCount !== 1 ? 's' : ''}`);
    }
    doc.moveDown(0.6);
    drawDivider(doc);

    if (!planDay) {
      doc.fill('#6b7280').fontSize(12).text('No meal plan found for today.', { align: 'center' });
    } else {
      // Resolve items per meal: prefer mealItems array, fall back to legacy single fields
      function getMealItems(day, mealType) {
        const fromMealItems = (day.mealItems || [])
          .filter(mi => mi.meal_type === mealType && mi.menuItem)
          .map(mi => mi.menuItem);
        if (fromMealItems.length > 0) return fromMealItems;
        if (mealType === 'breakfast' && day.breakfast) return [day.breakfast];
        if (mealType === 'lunch' && day.lunch) return [day.lunch];
        if (mealType === 'dinner' && day.dinner) return [day.dinner];
        return [];
      }

      const meals = [
        { label: 'Breakfast', time: '7:30 AM', mealType: 'breakfast', color: '#f97316' },
        { label: 'Lunch', time: '12:30 PM', mealType: 'lunch', color: '#3b82f6' },
        { label: 'Dinner', time: '7:30 PM', mealType: 'dinner', color: '#8b5cf6' }
      ];

      let totalCal = 0;

      meals.forEach(meal => {
        if (doc.y > 660) { doc.addPage(); doc.y = 50; }

        const items = getMealItems(planDay, meal.mealType);

        doc.fill(meal.color).fontSize(13).font('Helvetica-Bold')
          .text(`${meal.label}  ·  ${meal.time}`);
        doc.moveDown(0.2);

        if (items.length > 0) {
          items.forEach(item => {
            const prep = (item.prep_time_minutes || 0) + (item.cook_time_minutes || 0);
            const calPer = item.calories_per_serving || 0;
            const calTotal = calPer * familyCount;
            totalCal += calTotal;
            doc.fill('#1f2937').fontSize(11).font('Helvetica-Bold').text(`• ${item.name}`);
            let meta = `  ${item.cuisine_type || ''}  ·  ${calTotal} kcal (${familyCount} members)`;
            if (familyCount > 1) meta += `  ·  ${calPer} kcal/person`;
            if (prep > 0) meta += `  ·  ${prep} min prep`;
            doc.fill('#6b7280').fontSize(9).font('Helvetica').text(meta);
            if (item.protein_g || item.carbs_g || item.fat_g) {
              const p = Math.round((item.protein_g || 0) * familyCount);
              const c = Math.round((item.carbs_g || 0) * familyCount);
              const f = Math.round((item.fat_g || 0) * familyCount);
              doc.fill('#9ca3af').fontSize(8)
                .text(`  P: ${p}g  C: ${c}g  F: ${f}g  (for ${familyCount})`);
            }
          });
        } else {
          doc.fill('#9ca3af').fontSize(10).font('Helvetica').text('Not planned');
        }

        doc.moveDown(0.6);
        drawDivider(doc, '#f3f4f6');
      });

      if (!totalCal) totalCal = planDay.total_calories || 0;

      doc.moveDown(0.3);
      doc.fill('#16a34a').fontSize(13).font('Helvetica-Bold')
        .text(`Total Day Calories: ${totalCal} kcal`, { align: 'center' });
    }

    doc.moveDown(2);
    doc.fill('#9ca3af').fontSize(8).font('Helvetica')
      .text(`Generated by CookConnect · ${new Date().toLocaleDateString('en-IN')}`, { align: 'center' });

    doc.end();
    stream.on('finish', () => resolve({ filename, filepath }));
    stream.on('error', reject);
  });
}

