// ml per 1 unit of each volume measure
const VOLUME_TO_ML = {
  cup: 240, cups: 240,
  tbsp: 15, tablespoon: 15, tablespoons: 15,
  tsp: 5, teaspoon: 5, teaspoons: 5,
  'fl oz': 30, 'fluid ounce': 30, 'fluid ounces': 30,
  pint: 473, pints: 473,
  quart: 946, quarts: 946,
  litre: 1000, liter: 1000, litres: 1000, liters: 1000, l: 1000,
  ml: 1, milliliter: 1, milliliters: 1, millilitre: 1, millilitres: 1,
};

// grams per 1 cup for common dry/semi-dry ingredients (Indian + general)
const G_PER_CUP = {
  rice: 185, 'basmati rice': 185, 'brown rice': 185,
  flour: 120, 'wheat flour': 120, atta: 120, maida: 110, besan: 92, 'gram flour': 92,
  sugar: 200, 'powdered sugar': 120, 'icing sugar': 120, 'brown sugar': 220,
  salt: 292,
  oats: 90,
  dal: 190, lentils: 190, 'chana dal': 200, 'moong dal': 210, 'urad dal': 210,
  'toor dal': 200, 'masoor dal': 190,
  chickpeas: 200, 'chana': 200, 'rajma': 190, 'kidney beans': 190,
  paneer: 250,
  poha: 80, 'flattened rice': 80,
  semolina: 167, suji: 167, rava: 167, sooji: 167,
  'rolled oats': 90,
  cornmeal: 156,
  quinoa: 170,
  millet: 200,
  sago: 160, sabudana: 160,
  coconut: 80, 'desiccated coconut': 80,
  breadcrumbs: 108,
};

// Convert a quantity+unit to metric { qty, unit }
function toMetric(qty, unit, ingredientName) {
  const u = (unit || '').toLowerCase().trim();
  const name = (ingredientName || '').toLowerCase().trim();

  const mlFactor = VOLUME_TO_ML[u];
  if (!mlFactor) return { qty, unit }; // already g / kg / piece / or unknown — leave as-is

  const totalMl = qty * mlFactor;

  // Try density lookup: check ingredient name against known keys
  for (const [key, gPerCup] of Object.entries(G_PER_CUP)) {
    if (name.includes(key)) {
      const grams = Math.round((totalMl / 240) * gPerCup);
      return { qty: grams, unit: 'g' };
    }
  }

  // No density found — return as ml (or L if large)
  if (totalMl >= 1000) return { qty: parseFloat((totalMl / 1000).toFixed(2)), unit: 'L' };
  return { qty: Math.round(totalMl), unit: 'ml' };
}

function parseQty(str) {
  if (!str) return null;
  const s = str.trim();
  // Mixed: "1 1/2"
  const mixed = s.match(/^(\d+)\s+(\d+)\/(\d+)/);
  if (mixed) return parseInt(mixed[1]) + parseInt(mixed[2]) / parseInt(mixed[3]);
  // Fraction: "1/2"
  const frac = s.match(/^(\d+)\/(\d+)/);
  if (frac) return parseInt(frac[1]) / parseInt(frac[2]);
  // Plain number or decimal
  const num = s.match(/^(\d+\.?\d*)/);
  if (num) return parseFloat(num[1]);
  return null;
}

function extractUnit(str) {
  if (!str) return '';
  return str.trim().replace(/^[\d\s\/\.]+/, '').trim();
}

function formatQty(num, unit) {
  // Metric units — just show the number cleanly
  const metricUnits = ['g', 'kg', 'ml', 'l', 'L'];
  if (unit && metricUnits.includes(unit)) {
    return Number.isInteger(num) ? String(num) : num.toFixed(1);
  }
  // Non-metric — keep fraction symbols for readability
  if (Number.isInteger(num)) return String(num);
  const frac = num - Math.floor(num);
  const whole = Math.floor(num);
  const fracs = { 0.25: '¼', 0.5: '½', 0.75: '¾', 0.33: '⅓', 0.67: '⅔' };
  const nearest = Object.keys(fracs).reduce((a, b) => Math.abs(b - frac) < Math.abs(a - frac) ? b : a);
  if (Math.abs(frac - nearest) < 0.05) {
    return whole > 0 ? `${whole} ${fracs[nearest]}` : fracs[nearest];
  }
  return num.toFixed(1);
}

function consolidateIngredients(planDays, familyCount) {
  const map = new Map(); // key: lowercase name

  planDays.forEach(day => {
    // Prefer mealItems array; fall back to legacy single fields
    let mealItemsList = (day.mealItems || []).map(mi => mi.menuItem).filter(Boolean);
    if (mealItemsList.length === 0) {
      mealItemsList = [day.breakfast, day.lunch, day.dinner].filter(Boolean);
    }
    const meals = mealItemsList;
    meals.forEach(meal => {
      if (!meal.ingredients || !meal.ingredients.length) return;
      meal.ingredients.forEach(ing => {
        const key = ing.name.toLowerCase().trim();
        const rawUnit = ing.unit || extractUnit(ing.quantity) || '';
        const num = parseQty(ing.quantity);

        if (!map.has(key)) {
          map.set(key, { name: ing.name, unit: rawUnit, total: null, unparsed: [], notes: ing.notes || '' });
        }
        const entry = map.get(key);

        if (num !== null) {
          // Convert to metric before accumulating
          const { qty: metricQty, unit: metricUnit } = toMetric(num * familyCount, rawUnit, ing.name);
          if (entry.total === null) {
            entry.total = metricQty;
            entry.unit = metricUnit;
          } else {
            entry.total += metricQty;
            // If units differ after conversion (shouldn't happen for same ingredient), keep existing
          }
        } else {
          entry.unparsed.push(`${ing.quantity}${rawUnit ? ' ' + rawUnit : ''} ×${familyCount}`);
        }
      });
    });
  });

  return Array.from(map.values()).sort((a, b) => a.name.localeCompare(b.name));
}

async function generateShoppingListPDF(user, planDays, familyCount) {
  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({ margin: 50, size: 'A4' });
    const dir = ensurePdfDir();
    const filename = `shopping-list-${user.id}-${Date.now()}.pdf`;
    const filepath = path.join(dir, filename);
    const stream = fs.createWriteStream(filepath);
    doc.pipe(stream);

    const sorted = [...planDays].sort((a, b) => a.day_number - b.day_number).slice(0, 7);
    const startDate = sorted[0] ? formatDate(sorted[0].day_date) : '';
    const endDate = sorted[sorted.length - 1] ? formatDate(sorted[sorted.length - 1].day_date) : '';
    const items = consolidateIngredients(sorted, familyCount);

    // Header
    doc.rect(0, 0, doc.page.width, 75).fill('#0ea5e9');
    doc.fill('white').fontSize(22).font('Helvetica-Bold').text('CookConnect', 50, 18);
    doc.fontSize(11).font('Helvetica').text('Weekly Shopping List', 50, 48);
    doc.y = 95;

    doc.fill('#1f2937').fontSize(13).font('Helvetica-Bold')
      .text(`Family: ${user.family_name || user.name}`);
    doc.fill('#6b7280').fontSize(10).font('Helvetica')
      .text(`Week: ${startDate} — ${endDate}  ·  ${familyCount} member${familyCount !== 1 ? 's' : ''}`);
    doc.moveDown(0.5);
    doc.moveTo(50, doc.y).lineTo(doc.page.width - 50, doc.y).strokeColor('#e5e7eb').stroke();
    doc.moveDown(0.5);

    if (items.length === 0) {
      doc.fill('#6b7280').fontSize(11).text('No ingredients found in this plan.', { align: 'center' });
    } else {
      // Column headers
      const col1 = 50, col2 = 300, col3 = 430;
      doc.fill('#6b7280').fontSize(9).font('Helvetica-Bold');
      doc.text('INGREDIENT', col1, doc.y);
      doc.text('QUANTITY', col2, doc.y - doc.currentLineHeight());
      doc.text('NOTES', col3, doc.y - doc.currentLineHeight());
      doc.moveDown(0.2);
      doc.moveTo(50, doc.y).lineTo(doc.page.width - 50, doc.y).strokeColor('#d1d5db').stroke();
      doc.moveDown(0.3);

      items.forEach((item, i) => {
        if (doc.y > 750) { doc.addPage(); doc.y = 50; }

        const rowY = doc.y;
        const bg = i % 2 === 0 ? '#f9fafb' : '#ffffff';
        doc.rect(48, rowY - 2, doc.page.width - 96, 18).fill(bg);

        // Quantity display
        let qtyStr = '';
        if (item.total !== null) {
          qtyStr = `${formatQty(item.total, item.unit)} ${item.unit}`.trim();
        } else if (item.unparsed.length) {
          qtyStr = item.unparsed.join(', ');
        } else {
          qtyStr = 'as needed';
        }

        doc.fill('#1f2937').fontSize(10).font('Helvetica').text(item.name, col1, rowY, { width: 240 });
        doc.fill('#0ea5e9').font('Helvetica-Bold').text(qtyStr, col2, rowY, { width: 120 });
        doc.fill('#9ca3af').font('Helvetica').fontSize(9).text(item.notes || '', col3, rowY, { width: 110 });

        doc.y = rowY + 18;
      });
    }

    doc.moveDown(1.5);
    doc.moveTo(50, doc.y).lineTo(doc.page.width - 50, doc.y).strokeColor('#e5e7eb').stroke();
    doc.moveDown(0.5);
    doc.fill('#9ca3af').fontSize(8).font('Helvetica')
      .text(
        `Generated by CookConnect · ${new Date().toLocaleDateString('en-IN')} · Quantities for ${familyCount} member${familyCount !== 1 ? 's' : ''}`,
        { align: 'center' }
      );

    doc.end();
    stream.on('finish', () => resolve({ filename, filepath }));
    stream.on('error', reject);
  });
}

